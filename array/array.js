const array = [21 ,6, 84, 24 ,86 ,11]

//ARRAY -> MÉTODO  COMUNS
//INSIRIR DADOS
array.push(5) //ACRESCENTAR o dado 5 no FINAL do array
console.log(array)
array.unshift(5) // ACRESCENTAR o dado no COMEÇO do array
console.log(array)
//REMOVER DADOS
array.pop() // vai REMOVER um dado no FINAL, por isso não precisa de nenehum parametro
console.log(array)
array.shift()
console.log(array) // vai REMOVER um dado no COMEÇO, por isso não precisa de nenehum parametro